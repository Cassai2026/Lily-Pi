import time

class MissionBroadcastSovereign:
    def __init__(self):
        self.output_dir = "enki_ai/game_engine/outputs"
        if not os.path.exists(self.output_dir):
            os.makedirs(self.output_dir)

    def generate_legacy_report(self, mission_title, steps_completed):
        """Part 1 & 2: Auto-Documentation and Professional Formatting."""
        print(f"\n[BROADCAST] 📡 PREPARING MISSION REPORT: {mission_title}")
        
        report = f"""
# SOVEREIGN MISSION REPORT: {mission_title}
## NODE: 29 | PROJECT TYPE: Titan-Spec
---
### MISSION LOGS:
"""
        for step in steps_completed:
            report += f"- [x] {step}\n"
            
        report += "\n---\n*Generated by Enki-AI for the 29th Node. OUSH.*"
        
        file_path = os.path.join(self.output_dir, f"{mission_title.replace(' ', '_')}_README.md")
        with open(file_path, 'w') as f:
            f.write(report)
            
        print(f"[HUD] ✅ FORMATTING COMPLETE: {file_path}")
        return file_path

